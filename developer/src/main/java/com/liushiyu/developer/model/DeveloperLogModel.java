package com.liushiyu.developer.model;

public class DeveloperLogModel {

    private String developerLogTime;
    private int developerLogType;
    private String developerLogTag;
    private String developerLogContent;

    public String getDeveloperLogTime() {
        return developerLogTime;
    }

    public void setDeveloperLogTime(String developerLogTime) {
        this.developerLogTime = developerLogTime;
    }

    public int getDeveloperLogType() {
        return developerLogType;
    }

    public void setDeveloperLogType(int developerLogType) {
        this.developerLogType = developerLogType;
    }

    public String getDeveloperLogTag() {
        return developerLogTag;
    }

    public void setDeveloperLogTag(String developerLogTag) {
        this.developerLogTag = developerLogTag;
    }

    public String getDeveloperLogContent() {
        return developerLogContent;
    }

    public void setDeveloperLogContent(String developerLogContent) {
        this.developerLogContent = developerLogContent;
    }
}
